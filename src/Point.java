public class Point {
	
	private Integer xValue;
	private Integer yValue;
	
	public Integer getXValue(){
		return this.xValue;
	}
	
	public void setXValue(Integer newX){
		this.xValue = newX;
	}
	
	public Integer getYValue(){
		return this.yValue;
	}
	
	public void setYValue(Integer newY){
		this.yValue = newY;
	}

}
